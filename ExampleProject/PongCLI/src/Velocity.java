
public class Velocity {
    public float X;
    public float Y;

    public  Velocity(float  x,float  y){
        this.X = x;
        this.Y = y;
    }
    public  Velocity(float number){
        this.X = number;
        this.Y = number;
    }
    public  Velocity(){
        this.X = 0.0f;
        this.Y = 0.0f;
    }
}
